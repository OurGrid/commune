package br.edu.ufcg.lsd.commune.functionaltests.data.invokeservice;

public class SerializableObject implements ExtendsSerializable {

	
	private static final long serialVersionUID = 1L;

	
	private String value;


	
	public SerializableObject() {}


	public SerializableObject(String value) {
		this.value = value;
	}


	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		final SerializableObject other = (SerializableObject) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}
}