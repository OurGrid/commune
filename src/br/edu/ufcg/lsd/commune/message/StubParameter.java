package br.edu.ufcg.lsd.commune.message;

import java.io.Serializable;

import br.edu.ufcg.lsd.commune.CommuneRuntimeException;
import br.edu.ufcg.lsd.commune.identification.DeploymentID;

public class StubParameter implements Serializable {

	private static final long serialVersionUID = 1L;

	private final DeploymentID id;
	
	public StubParameter(DeploymentID id) {
		if (id == null) {
			throw new CommuneRuntimeException("Stub parameter with null id");
		}
		this.id = id;
	}

	public DeploymentID getId() {
		return id;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		final StubParameter other = (StubParameter) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
	@Override
	public String toString() {
		return id.toString();
	}
}